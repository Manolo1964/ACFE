        <h2>Les catégories de vélos</h2>
        <br/>
          <article class="item">
            <img class="img" src="Images\v_route.jpg"
            alt="photo velo route" />          
            <h3>Le vélo de route</h3>
            <p>Ce modèle est très apprécié des sportifs, car il est taillé pour la compétition et les pistes accidentées. Il occupe une place majeure dans le secteur du cyclisme de loisir.</p>
          </article> 
          <article class="item">
            <img class="img" src="Images\v_urbain.jpg"
            alt="photo velo v_urbain" />          
            <h3>Le vélo urbain</h3>
            <p>Il est pourvu d’un cadre dont l’assise offre une posture et une hauteur aux cyclistes. Cela leur permet d’avoir une vue dégagée sur le trafic.</p>
          </article>
          <article class="item">
            <img class="img" src="Images\vtt.jpg"
            alt="photo velo vtt" />          
            <h3>Le vélo tout chemin ou VTC</h3>
            <p>Comme son nom l’indique, c’est un modèle polyvalent qui associe le confort du vélo de ville à la souplesse des vélos généralement destinés à rouler sur les pistes campagnardes. Cet hybride est aussi appelé « vélo trekking ».</p>
          </article>
          <article class="item">
            <img class="img" src="Images\v_ebike.jpg"
            alt="photo velo e-bike"/>          
            <h3>Le vélo électrique ou VAE</h3>
            <p>Ce vélo est pourvu d’un petit moteur et d’une batterie qui alimentent l’assistance électrique et aide ainsi le cycliste durant le pédalage. Il ne remplace pas totalement la force du cycliste, mais vient en renfort pour que ce dernier n’ait pas à déployer toute sa force. Sans pédalage, ce modèle n’avance pas, mais permet quand même de faire du sport en mode plus « soft ».</p>
          </article>
